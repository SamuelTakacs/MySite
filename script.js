
function pondelok() {
    let uto = document.getElementById("hid-uto");
    let str = document.getElementById("hid-str");
    let stv = document.getElementById("hid-stv");
    let pon = document.getElementById("hid-pon");
    let pia = document.getElementById("hid-pia");
    let sob = document.getElementById("hid-sob");
    let ned = document.getElementById("hid-ned");
    if (pon.style.display === "block") {
        pon.style.display = "none";
    } else {
        pon.style.display = "block";
    };
    if (uto.style.display === 'block') {
        uto.style.display = "none";
    };
    if (str.style.display === 'block') {
        str.style.display = "none";
    };
    if (stv.style.display === 'block') {
        stv.style.display = "none";
    };
    if (pia.style.display === 'block') {
        pia.style.display = "none";
    };
    if (sob.style.display === 'block') {
        sob.style.display = "none";
    };
    if (ned.style.display === 'block') {
        ned.style.display = "none";
    }; 
};

